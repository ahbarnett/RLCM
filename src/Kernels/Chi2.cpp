#include "Chi2.hpp"

#define INITVAL_s DBL_MAX


//--------------------------------------------------------------------------
Chi2::
Chi2() {
  Init();
}


//--------------------------------------------------------------------------
void Chi2::
Init(void) {
  ReleaseAllMemory();
  s = INITVAL_s;
}


//--------------------------------------------------------------------------
void Chi2::
ReleaseAllMemory(void) {
}


//--------------------------------------------------------------------------
void Chi2::
Init(double s_) {
  s = s_;
}


//--------------------------------------------------------------------------
Chi2::
Chi2(double s_) {
  Init(s_);
}


//--------------------------------------------------------------------------
Chi2::
Chi2(const Chi2 &G) {
  Init();
  DeepCopy(G);
}


//--------------------------------------------------------------------------
Chi2& Chi2::
operator= (const Chi2 &G) {
  if (this != &G) {
    DeepCopy(G);
  }
  return *this;
}


//--------------------------------------------------------------------------
void Chi2::
DeepCopy(const Chi2 &G) {
  s = G.s;
}


//--------------------------------------------------------------------------
Chi2::
~Chi2() {
  ReleaseAllMemory();
}


//--------------------------------------------------------------------------
double Chi2::
GetS(void) const {
  return s;
}
